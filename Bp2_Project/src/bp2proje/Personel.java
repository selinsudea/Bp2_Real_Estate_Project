public class Personel {

    int id;
    String name;
    String surname;
    String type;
    String username;
    String email;
    String password;

    public Personel() {
    }

    public Personel(int id, String name, String surname, String type, String username, String email, String password) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.type = type;
        this.username = username;
        this.email = email;
        this.password = password;
    }

}
